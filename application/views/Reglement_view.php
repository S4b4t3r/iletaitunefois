        <h1>Règlement d'ordre intérieur</h1>
        <p>Nous demandons à tous nos membres de bien vouloir prendre connaissance de notre règlement d'ordre intérieur et de s'y conformer dans le respect de tous.</p>

        <section class="aide-section">
            <h2>Article 1</h2>
            <p class="dontindent">
                Le Coin de Lecture « Il était une fois… » est un service ouvert au public destiné à toute la population (sous condition d'adhérer par une cotisation annuelle à notre association ). Pour en faciliter l’accès, il comprend une section pour la consultation sur place des collections à des fins d’information, d’enrichissement culturel et de loisirs et une section pour les ateliers de lecture et de bricolage, ainsi que pour les Sing Along et Cours de musique.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 2</h2>
            <p class="dontindent">
                L’accès au Coin de Lecture, la consultation sur place des documents écrits, de même que le prêt de livres et le déroulement des ateliers répondent aux modalités qui suivent.
            </p>
        </section>
        
        <section class="aide-section">
            <h2>Article 3</h2>
            <p class="dontindent">
                Les services du Coin de Lecture ne sont pas tous gratuits. Pour en connaître les détails veuillez consulter le ‘Règlement-taxe’ dans la rubrique <a href="<?php echo base_url()?>adhesion"><b>Adhésion</b></a>.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 4</h2>
            <p class="dontindent">
                Le prêt à domicile n’est consenti qu’aux personnes inscrites comme usagers et résidant au Grand-Duché de Luxembourg ou dans la Grande Région.
            </p>
            <p>
                L’adhésion coûte 30 € par famille et par an. Elle est attestée par une carte d’utilisateur indiquant le nom, le numéro de carte de membre, de même que la date d’expiration faite après avoir rempli et signé dûment le formulaire.
            </p>
            <p>
                Sur demande, l’usager doit justifier de l’exactitude de ces données moyennant sa carte d’identité ou toute autre pièce officielle. Tout changement d’adresse est à communiquer sans retard au personnel du Coin de Lecture.
            </p>
            <p>
                Pour les jeunes de moins de 16 ans, la carte afférente doit être contresignée par la personne investie de l’autorité parentale, de même que doivent être fournis le nom, prénom et adresse de ladite personne, ainsi qu’un numéro de téléphone permettant de la joindre. Par sa contre-signature, cette personne s’engage à tenir le Coin de Lecture quitte et indemne de tout dommage de quelque nature causé par la personne mineure dont il est responsable, de même qu’à la restitution des documents prêtes dans le délai imparti et au paiement des taxes dues en cas de restitution tardive.
            </p>
            <p>
                La présentation de la carte d’utilisateur est exigée pour toute opération de prêt.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 5</h2>
            <p class="dontindent">
                Les heures d’ouverture sont dûment affichées à l’entrée du Coin de Lecture qui sera fermé pendant les vacances scolaires et jours fériés.
                Tout utilisateur est tenu de se comporter de manière à ne pas troubler la tranquillité des autres personnes présentes.
                Il est notamment interdit : </br>
                    - de fumer, <br/>
                    - de consommer de l’alcool.
            </p>
            <p>
                Il est en outre interdit d’entrer dans le Coin de Lecture en état d’ébriété ou sous l’emprise de stupéfiants.
            </p>
            <p>
                Il est interdit d’y amener des animaux, sauf les chiens d’aveugle accompagnant des personnes non-voyantes.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 6</h2>
            <p class="dontindent">
                L’affichage dans les espaces ouverts au public est soumis à l’autorisation du responsable du Coin de Lecture.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 7</h2>
            <p class="dontindent">
                Les utilisateurs de la bibliothèque sont responsables des livres ou des ouvrages qu’ils empruntent, de même qu’ils doivent prendre soin du matériel et des équipements du Coin de Lecture, y compris les instruments fournis pendant les cours de musique. En cas de perte ou dommages de toute pièce d'équipement, l'utilisateur devra payer intégralement et sans retard le coût du remplacement.
            </p>
            <p>
                De plus, lors de la consultation des documents de la bibliothèque, il est interdit de :</br>
                    - écrire, dessiner ou faire des marques sur ou à l'intérieur des livres;</br>
                    - arracher, déchirer, couper, plier les pages ou le coin des pages de livres;</br>
                    - dégrader de quelque manière que ce soit le matériel ou l'équipement mis à disposition.
            </p>
            <p>
                Vous êtes invité à retourner tous les livres qui ont été examinées dans la Boîte Magique.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 8</h2>
            <p class="dontindent">
                Le prêt à domicile des livres est consenti pour une durée maximale de 2 semaines renouvelable sur demande écrite ou téléphonique une fois pour une même durée sous réserve que le livre ne soit pas sollicité par un autre usager. Le nombre maximal de livres empruntables moyennant une carte d’utilisateur est limité à 2 livres par enfant. Le retard de retour de livres est sanctionné par une amende de 2€ par livre et par semaine de retard pour un maximum de 4 semaines.
            </p>
            <p>
                Au-delà de 4 semaines de retard, en plus de l’amende, il sera également demandé à l’utilisateur en faute de rembourser le prix du/des livre(s) + 5€ (frais administratifs) par livre.
            </p>
            <p>
                Les utilisateurs doivent s’abstenir de faire eux-mêmes des réparations aux livres empruntés.
            </p>
            <p>
                Certains livres sont exclus du prêt, notamment :</br>
                    - les livres de décoration,</br>
                    - les livres de grande taille.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 9</h2>
            <p class="dontindent">
                Le prêt à domicile des livres est gratuit pendant la période indiquée à l’article 8. Ce délai est à observer strictement et les retards sont sanctionnés par une taxe de restitution tardive fixée au ‘Règlement-taxe’.
            </p>
            <p>
                En cas de retard de restitution répété, la direction peut suspendre le droit de prêt jusqu’à l’acquittement de la taxe prévue à l’alinéa qui précède.
            </p>
            <p>
                En cas de perte ou dégradation d’un livre, l’utilisateur est tenu d’en rembourser le prix de remplacement sur facture et sur première demande.
            </p>
            <p>
                Les livres emportés à domicile ne peuvent en aucun cas être prêtés à des tierces personnes.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 10</h2>
            <p class="dontindent">
                La reproduction et l’utilisation des livres sont soumises au respect de la législation en vigueur, en particulier de la législation sur les droits d’auteur. Le Coin de Lecture ne saurait en aucun cas être tenu pour responsable d’un usage contrevenant à cette législation.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 11</h2>
            <p class="dontindent">
                Pour participer aux activités proposées par l’équipe du Coin de Lecture, il est obligatoire d’être membre. L’inscription est indispensable et ne sera validée qu’après paiement préalable par virement bancaire ou sur place en cash. L’inscription sera ensuite confirmée par mail. Toute demande d’annulation doit nous parvenir 2 semaines avant la date de l’atelier, 50% du coût d’inscription seront reversés. Veuillez noter que les absences et les annulations tardives ne sont pas remboursées.
            </p>
            <p>
                La participation est payante au prix de 8 euros ( prix variable selon les projets ).
            </p>
            <p>
                Les parents sont priés de rester avec leur(s) enfant(s) durant les activtés, à moins de signer le formulaire de décharge.
            </p>
            <p>
                La durée des activités varie de 30 minutes à 1 heure 1 /2.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 12</h2>
            <p class="dontindent">
                Tout utilisateur est responsable de ses effets personnels dans l'enceinte du Coin de Lecture. Ce dernier ne saurait en aucun cas être tenu responsable en cas de perte ou de vol occasionné dans ses locaux.
            </p>
        </section>

        <section class="aide-section">
            <h2>Article 13</h2>
            <p class="dontindent">
                Sans préjudice des peines plus fortes prévues par la loi, les infractions au présent règlement sont punies d’une peine de police.
            </p>
        </section>
