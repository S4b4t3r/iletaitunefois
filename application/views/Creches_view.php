            <h2>Visites</h2>
            <h3>Quels sont les avantages de notre bibliothèque ?</h3>
            <p>En Janvier 2010, création du Coin de Lecture sis au 6, rue Tony Bourg à Gasperich, composé d’un espace bibliothèque et d’un espace réservé aux activités. Pourquoi? La plupart des bibliothèques du pays proposent des livres pour enfants dans les 3 langues officielles du pays (luxembourgeois, français et allemand). Etant donné la grande diversité culturelle du Luxembourg, bon nombre de langues n’y sont guère représentées. D ’autre part, bien qu’une grande majorité des bibliothèques disposent d’espaces réservés aux enfants, elles manquent de personnel pouvant se dédier pleinement à leurs besoins et ainsi, animer et accompagner la lecture des enfants. Nous sommes convaincues que l’animation autour du livre est une mission essentielle d’une bibliothèque enfantine afin de contribuer au développement de la culture et du langage. De plus, au niveau structurel, le coin enfants est souvent à proximité de la section adultes. Les enfants sont donc priés et obligés de respecter le silence, inhibant ainsi leur spontanéité et leur intérêt. Notre défi est de changer le concept même de la bibliothèque, entendue comme un espace où règne le silence, en proposant un lieu convivial, ouvert et vivant, un lieu de rencontres et d’échanges.</p>
