            <h2>Sing Along</h2>
            <h3>Que sont les sessions Sing Along ?</h3>
            <p>Les séances de Sing-Along durent environ 60 minutes et sont destinées aux enfants âgés de 0 à 5 ans. Le choix du matériel proposé aide les enfants à maintenir un lien avec leur langue et leur culture maternelle. Tandis que, dans le même temps, le langage et le développement moteur ainsi que l'attachement positif sont favorisés. Chaque session est planifiée afin de respecter la créativité de la personne qui offre le service et pour préserver l'élément culturel, spécifique à chaque langue. D'autre part, et lorsque l'activité est offerte dans une variété de langues, le format doit être cohérent dans la sélection d'un répertoire qui permettra le développement de compétences particulières spécifiques à l'âge, adapté à l'âge indépendamment de la langue.</p>
            
            <h3>Quels sont les objectifs des sessions Sing-Along ?</h3>
            <p>Le développement du langage et de l'imaginaire.<br>
            Le développement des facultés motrices.<br>
            L'identité culturelle.<br>
            Les aptitudes sociales.<br>
            Les liens sociaux et l’attachement positif.
            </p>

            <h3>Quels sont les avantages des sessions Sing-Along ?</h3>
            <p>Socialisation.<br>
            Confiance en soi et indépendance.<br>
            Expression émotionnelle à travers le chant.<br>
            Ecoute, concentration, créativité et séquençage.<br>
            Apprendre à travers l'émulation.
            </p>

