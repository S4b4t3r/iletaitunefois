        <h1>Langue Maternelle</h1>

        <section>
            <p>La langue maternelle fait partie intégrante de la personnalité de chacun, en tant que premier véhicule de nos sentiments et outil de communication et d’identification avec une communauté.<br/>
                <br/>
                La langue maternelle est celle que nous maîtrisons le mieux et dont nous connaissons les subtilités d’usage; elle est donc le moyen le plus direct et le plus naturel de nous faire comprendre et ainsi communiquer avec nuances nos émotions et nos idées. Il n’est donc pas étonnant que les enfants se sentent plus à l’aise avec leur langue maternelle. De plus, il est reconnu qu’un enfant qui maîtrise sa langue maternelle apprend plus facilement les langues étrangères.<br/>
                <br/>
                Au-delà du cercle familial, les enfants d’origine étrangère n’ont que très rarement l’occasion de parler, lire et écouter leur langue maternelle. Un "coin de lecture", en tant que lieu éducatif et de rencontre, est le lieu idéal pour valoriser son importance et faciliter son apprentissage et son intégration.</p>
