            <h2>Partenariat officiel</h2>
            <p>A partir d’un certain montant annuel, une entreprise (ou une fondation) devient partenaire officiel de « Il était une fois… asbl ».<br/>
                Elle recevra automatiquement une attestation de notre part, une invitation à toutes nos manifestations (Carnaval, Pâques, Noël, fête de fin d’année) ainsi qu'un compte-rendu trimestriel détaillé de toutes nos dépenses et projets en cours.<br/>
                Elle pourra, si elle le souhaite, afficher notre label tant dans sa communication interne qu’externe.<br/>
                De notre côté, nous assurerons la publicité du partenariat de ces entreprises, et nous mettrons en avant nos partenaires solidaires de notre cause sur nos publications mais également sur nos réseaux sociaux.</p>
